package Inheritance;

public class InheritanceDocs {
	/*
	 * =================== Inheritance ======================================
	 * 
	 *   # code re-usability
	 *   # process of accessing properties and methods of parent/super/base
	 *     class to child/sub/derived class is known as inheritance
	 *     
	 *   # also called IS-A relationship
	 *   # Its a generalization to specialization process. 
	 *     eg: vehicle is generalization and Mercedes car is specialization term.
	 *     
	 *   # we can inherit static properties and method also
	 *   # we cann't inherit private properties, method and constructors.
	 *   # we cann't inherit final class.
	 *   # we can inherit only one class at a time.
	 *   # 'extends' keyword is used for inheritance.
	 *   
	 *   syntax: 
	 *    
	 *    class child_class_name  extends parent_class{
	 *    
	 *    // properties : parent + child
	 *    
	 *    // methods : parent + child 
	 *    
	 *    }
	 *  
	 */

}
