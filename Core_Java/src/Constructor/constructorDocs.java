package Constructor;

public class constructorDocs {

	
	/*
	 * ==================== Constructor ========================================
	 * 
	 *  # Use to create object in java.
	 *  # special type of method which has same name as class name.
	 *  # no return type but return value...
	 *  # if there is no user defined constructor in class then java provides
	 *    default constructor to create object.
	 *  
	 *  # Types :
	 *  
	 *    1> Default Constructor:
	 *     -----------------------
	 *    	-> use to create object and default value to it.
	 *    
	 *    	syntax:
	 *         class_name(){
	 *         
	 *         //set default value to object
	 *         }
	 *    
	 *    	
	 *    2> Parameterized Constructor: 
	 *      ---------------------------
	 *      -> use to create object and set dynamic value to it.
	 *      
	 *       syntax:
	 *         class_name(arg1,arg2,arg3,...,argn){
	 *         
	 *          //set dynamic values to object
	 *         }
	 * 
	 */
	
	
	
}
